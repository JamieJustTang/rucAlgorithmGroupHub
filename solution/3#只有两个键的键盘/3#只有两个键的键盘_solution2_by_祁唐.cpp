void genPrimes(int n){
    vector<int> primes;
    for(int i=2;i<n;i++){
        int flag=1;
        for(int j=0;j<primes.size();j++){
            if(i%primes[j]==0){
                flag=0;
                break;
            }
        }
        if(flag==1) primes.push_back(i);
    }
    for(int j=0;j<primes.size();j++)
        cout<<primes[j]<<",";
    cout<<endl;
}

